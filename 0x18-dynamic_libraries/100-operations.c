/**
 * add - adds two ints
 * @a: first int
 * @b: second int
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - finds diff of two ints
 * @a: first int
 * @b: second int
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two ints
 * @a: first int
 * @b: second int
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two ints
 * @a: first int
 * @b: second int
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - return remainder of a / b
 * @a: first int
 * @b: second int
 * Return: a // b
 */
int mod(int a, int b)
{
	return (a % b);
}
